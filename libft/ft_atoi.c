#include "libft.h"

int	ft_atoi(const char *str)
{
	long	res;
	int		sign;

	while (ft_isspace(*str))
		str++;
	sign = 1;
	if (*str == '-')
		sign = -1;
	if (*str == '+' || *str == '-')
		str++;
	res = 0;
	while (ft_isdigit(*str))
	{
		res = res * 10 + sign * (*str++ - '0');
	}
	return (res);
}
